#include <unistd.h>

void	ft_div_mod(int a, int b, int *div, int *mod)
{
	*div = a / b;
	*mod = a % b;
}

void	ft_putnbr(int n)
{
	char	c;

	if (n >= 10)
		ft_putnbr(n / 10);
	c = (n % 10) + '0';
	write(1, &c, 1);
}

int	main(void)
{
	int	a = 10;
	int	b = 3;
	int	div, mod;

	ft_div_mod(a, b, &div, &mod);
	ft_putnbr(div);
	write(1, "\n", 1);
	ft_putnbr(mod);
	return (0);
}
